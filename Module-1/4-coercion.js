//String concatenation
console.log("1" + 7); //"17"
console.log(2 + "4"); //"24"
console.log(2 + 2 + "4"); //"44"
console.log("2" + 2 + 2); //"222"
console.log("3" + "2"); //"32"

//String to Number
console.log(1 + +"2"); //3
console.log(1 + Number("2")); //3   //Number() is a function that converts the string to a number

//Numeric operations
console.log("3" - 2); //1
console.log("3" * "2"); //6
console.log("3" / "2"); //1.5

//Boolean operations
console.log(Boolean("")); //false
console.log(Boolean(0)); //false
console.log(Boolean("0")); //true
console.log(Boolean([])); //true

